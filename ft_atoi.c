int	ft_atoi(const char *str)
{
	const char	*i;
	int			a;
	int			b;

	i = str;
	a = 1;
	b = 0;
	while (*i == 32 || (*i >= 9 && *i <= 13))
		i++;
	if (*i == '+' || *i == '-')
	{
		if (*i == '-')
			a *= -1;
		i++;
	}
	while (*i >= '0' && *i <= '9')
	{
		b = b * 10 + (*i - '0');
		i++;
	}
	b *= a;
	return (b);
}
